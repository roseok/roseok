</b><br><br>
                Astrid doet projecten bij Yad Vashem, de Anne Frank stichting, University of South Carolina en Bosch Film. Op allerlei manieren houdt ze zich bezig met geschiedenis en het vertellen van verhalen. Tijdens haar studie liep Astrid stage in het holocaustmuseum in Israël. Hier verdiepte ze zich in de ervaring van Joodse mensen tijdens de oorlog. Ze luisterde naar de persoonlijke verhalen van oude mensen en bestudeerde talloze films en dagboeken. Allemaal om maar te begrijpen wat de oorlog met mensen doet.
                <br><br>
              Wat Astrid het liefste doet is het overbrengen van geschiedenisverhalen op een jong publiek. Bij de Anne Frank Stichting organiseerde Astrid educatieve projecten voor kinderen en jongeren. Hier vertelde ze niet alleen het verhaal van Anne Frank, maar besprak zij ook vooroordelen en conflicten die vandaag de dag bestaan. Op een middag gaf ze een rondleiding aan een groep Syrische studenten. Ze ontdekte hoezeer zij zich kunnen identificeren met Anne Frank en hoeveel het verhaal in hen losmaakte. De studenten wilden hun verhaal ook opschrijven, zoals Anne dat deed. Op dit moment realiseerde Astrid zich nog meer dan tevoren hoe universeel het ervaren van een oorlog kan zijn. Door ervaringen van anderen te lezen, leer je je eigen ervaring beter te begrijpen!
                <br><br>
                Toen er veel vluchtelingen uit Syrië naar Nederland kwamen, wilde Astrid graag iets bijdragen. Ze voelde zich betrokken bij het lot van de mensen die hun huizen hadden achtergelaten. Ze besloot op het centraal station in Amsterdam mee te helpen en ze ging op bezoek in een azc. Hier ontmoette ze een meisje dat haar haar kamer liet zien. Een Syrisch meisje dat de oorlog nog maar net ontvlucht was, zoals het personage van Laila in haar boek.

Met De brieven van Mia valt alles samen voor Astrid. Dat het dit verhaal zou worden, was een natuurlijk gegeven. De oude mensen die ze sprak over hun herinneringen aan de Tweede Wereldoorlog komen terug in haar personage van de Joodse meneer Cohen. De jonge mensen die ze ontmoette, die nog maar net de oorlog ontvluchtten, komen terug in het personage van de Syrische Laila. De vriendschap die Astrid tussen hen tot bloei laat komen, laat zien hoe ontzettend waardevol het uitwisselen van verhalen kan zijn.
