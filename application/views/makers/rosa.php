							<div id="attachment_234" style="width: 1034px" class="wp-caption alignnone"><img class="wp-image-234 size-full" src="http://www.rosestories.nl/wp-content/uploads/2014/10/Tegelafbeelding-beeld-in-tekst-Rosa-Vitalie.-Fotocredits-Masha-Osipova-edit.jpg" alt="Rosa Vitalie" width="1024" height="545" srcset="http://www.rosestories.nl/wp-content/uploads/2014/10/Tegelafbeelding-beeld-in-tekst-Rosa-Vitalie.-Fotocredits-Masha-Osipova-edit.jpg 1024w, http://www.rosestories.nl/wp-content/uploads/2014/10/Tegelafbeelding-beeld-in-tekst-Rosa-Vitalie.-Fotocredits-Masha-Osipova-edit-300x159.jpg 300w, http://www.rosestories.nl/wp-content/uploads/2014/10/Tegelafbeelding-beeld-in-tekst-Rosa-Vitalie.-Fotocredits-Masha-Osipova-edit-973x517.jpg 973w, http://www.rosestories.nl/wp-content/uploads/2014/10/Tegelafbeelding-beeld-in-tekst-Rosa-Vitalie.-Fotocredits-Masha-Osipova-edit-508x270.jpg 508w" sizes="(max-width: 1024px) 100vw, 1024px" /><p class="wp-caption-text">Foto&#8217;s: Masha Osipova</p></div>
<p><b><br />
Rosa Vitalie (1980) is een geboren en getogen Amsterdamse, waar ze in een bont gezelschap opgroeide en een liefde ontwikkelde voor andere culturen. Haar passie en talent voor creatie, illustratie en mooi beeld kwamen tot wasdom tijdens haar </b><b>opleiding Grafisch &amp; Typografisch Ontwerp aan de Koninklijke Academie van Beeldende Kunsten in Den Haag. Sinds haar afstuderen in 2004 heeft Rosa voor veel verschillende klanten gewerkt in de commerciële en non-profit sector. Voor ROSE stories is ze zowel verantwoordelijk voor de illustraties van <em>Mootje</em>, als voor de vormgeving van het voorleesboek <em>Hoekjes van geluk </em>en de kookboeken <em>Melk &amp; Dadels</em></b><em><b> </b></em><b>en <em>Maancake &amp; Jasmijn</em>.</b></p>
<p><span style="font-weight: 400;">Over de jaren heen heeft Rosa een voorkeur ontwikkeld voor opdrachten </span><span style="font-weight: 400;">waarmee ze het bewustzijn voor de schoonheid van dieren, planten en culturen kan versterken. De laatste jaren illustreert Rosa regelmatig voor het NRC Handelsblad</span><span style="font-weight: 400;">. Ze verzorgde de illustraties voor het boek </span><i><span style="font-weight: 400;">En de mens speelt met de tijd</span></i><span style="font-weight: 400;"> van </span><span style="font-weight: 400;">Allerd Stikkers </span><span style="font-weight: 400;">over het ontstaan van het heelal, het leven en het bewustzijn. Bij ROSE stories heeft </span><span style="font-weight: 400;">Rosa de vormgeving van </span><em>Melk &amp; Dadels &#8211; 100 geheime recepten van Marokkaanse moeders</em><span style="font-weight: 400;"> op zich genomen, alsmede van het Chinese vervolg erop. Daarnaast heeft ze de tekst van Maryam Hassouni en de illustraties van Ramona Treffers samengebracht in de vormgeving van het voorleesboek <em>Hoekjes van geluk</em>. Naast deze prettige samenwerking is Rosa één van de vrouwen achter</span><em><span style="font-weight: 400;"> Mootje</span></em><span style="font-weight: 400;">.<br />
</span></p>
<p><span style="font-weight: 400;">Rosa over <em>Mootje</em>: “Ik heb een relatie met een man uit Zuid-Duitsland. Dat klinkt heel dichtbij, maar de cultuurverschillen kunnen soms enorm zijn. We wonen samen met ons kindje in Amsterdam en ik zie de blijdschap die hij voelt als hij zijn eigen cultuur herkent in de winkel, in boeken, in het dagelijks leven. Hij deelt dat dan ook maar al te graag met ons! De behoefte om je kinderen een stukje van jezelf mee te geven heeft ieder mens, je kunt dat eigenlijk niet ontkennen. Daarom vind ik <i>Mootje</i></span><span style="font-weight: 400;"> zo’n belangrijk project: je achtergrond en </span><i><span style="font-weight: 400;">roots </span></i><span style="font-weight: 400;">zijn prachtig, ik hoop dat zoveel mogelijk kinderen dat meekrijgen.”</span></p>
