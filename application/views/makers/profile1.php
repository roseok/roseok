<p dir="ltr"><strong>Het grote publiek kent actrice <span class="il">Maryam</span> Hassouni vooral van haar rol in <em>Dunya &amp; Desie</em>. <span class="il">Maryam</span>s carrière bestaat echter uit nog veel meer hoogtepunten: zo won ze een Emmy Award voor beste actrice voor haar rol in de telefilm <em>Offers</em>, speelt ze een van de hoofdrollen in de films <em>Soof</em> en <em>Debiel</em> en is ze te zien in de televisieserie <em>Flikken Rotterdam</em>.</strong></p>
<p dir="ltr">Het voorleesboek <em>Hoekjes van geluk</em> kan aan Maryams inmiddels indrukwekkende cv worden toegevoegd. In een beeldend verhaal laat ze pechvogel Sam in verschillende magische werelden ontdekken dat geluk overal te vinden is. Het verhaal is geïnspireerd op <span class="il">Maryam</span>s zoektocht naar geluk in een moeilijke periode. &#8220;Ik heb mezelf op een heel kinderlijke manier eraan herinnerd wat voor mij nou eigenlijk mijn geluksmomenten zijn geweest. Ik herinnerde me dat geluk voelt alsof je hart wordt gekieteld en ook dat ik van suikerspinnen altijd heel gelukkig werd &#8211; en nog steeds!&#8221;</p>
<p dir="ltr"><em>Hoekjes van geluk</em> is haar debuut als auteur, maar het schrijven zit <span class="il">Maryam</span> al langer in het bloed. Een jaar voordat ze met het kinderboek begon, schreef ze haar eigen monoloog, <em>Een Teruggedachte</em>. Toch is het schrijven van <em>Hoekjes van Geluk</em> een van de meest bijzondere dingen die Maryam ooit heeft gedaan. Ze vindt het zoveel spannender dan op een set staan en acteren, omdat het zo persoonlijk is.</p>
<p dir="ltr">Met dit boek wil Maryam kinderen vertellen dat iedereen momenten kent die niet leuk zijn. &#8220;Ook al lijkt het alsof je er niet uitkomt: het komt uiteindelijk allemaal goed. Je bent niet de enige pechvogel.&#8221; Ze wil daarnaast dat iedereen zich herkent in dit verhaal, of je nu Marokkaans, Nederlands of Surinaams bent. &#8220;Ik zie dat er echt een gebrek is aan kinderboeken die een werkelijke weerspiegeling zijn van Nederland. Sam is universeel. Ieder kind moet zich in hem kunnen herkennen.&#8221;</p>
